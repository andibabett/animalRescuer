package org.fasttrackit;

public class Person {


    private String sex;
    private int age;

    public Person() {
        this.sex = sex;
        this.age = age;
    }

    // set and get
    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
